import { addUser, removeUser, getUser, getUsersFromRoom } from './clients/users.js';
import { createServer } from "http";
import { Server } from "socket.io";
import express from 'express';
import cors from 'cors';


const app = express();
const server = createServer(app);
const io = new Server(server, { cors: { origin: '*' } });


// middleware's
app.use(cors());
app.use(express.static('public'));
app.use('/fav.ico', express.static('public/fav.ico'));


// 1st) ðŸŸ© From server side, 
// socket connection start from here...
// ðŸ™„ all code run inside this function()
// when client hit into this server through URL ðŸ”— 
// then we received realtime connection/disconnection
io.on('connection', (socket) => {

    console.log('Client Open App... âœ…');

    // 1st) ðŸŸ¨ event create | & | listener method present at client side
    // Client listen this event by .emit() method... at client side  
    socket.on('join', ({ name, room }, callback) => {

        // 1.1) ðŸŸ¨ set user into users[array]
        const { error, user } = addUser({ id: socket.id, name, room })


        // 1.2) ðŸŸ¨ if error happen terminate this function
        if (error) return callback(error);

        // 1.5) ðŸŸ¨ join into chat room
        socket.join(user.room);

        // .emit() ==> all time listening events that generated from Client Side.
        // 1.3) ðŸŸ¨ simple welcome message from admin... 
        // when any new user come to join into the chat room.
        socket.emit('message', { user: 'admin', text: `${user.name}, welcome to the room ${user.room}` })


        // 1.4) ðŸŸ¨ broadcast => send the message everyone inside chat room...
        // but beside that specific user, who recently join into the chat room...
        socket.broadcast.to(user.room).emit('message', { user: 'admin', text: `${user.name}, has joined!` })

        io.to(user.room).emit('roomData', { room: user.room, users: getUsersFromRoom(user.room) });
        
        callback()
    });


    // send sms to ==> client
    socket.emit('demoTesting', 'Hello for server...');


    // 2nd) ðŸŸ¨ creating events, that generated by USER as chat messages...
    // waiting for event happening, form Client side by the help of .emit() method.
    // get | receive message from client...
    socket.on('sendMessage', (message, callback) => {

        // 2.1) ðŸŸ¨ get user id from socket.io system
        const user = getUser(socket.id);

        // 2.2) ðŸŸ¨ specify the chat room name & send message with the sender name
        io.to(user?.room).emit('message', { user: user?.name, text: message });

        callback()
    })


    socket.on('disconnect', () => {
        console.log('Client Close App... âŒ');
        const user = removeUser(socket.id);

        if (user) {
            io.to(user.room).emit('message', { user: 'Admin', text: `${user.name} has left.` });
            io.to(user.room).emit('roomData', { room: user.room, users: getUsersInRoom(user.room) });
        }
    })
});



